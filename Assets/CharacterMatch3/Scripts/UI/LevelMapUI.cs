        private float GetScrollYForLevel(int levelNumber)
        {
            var viewport = scrollRect != null && scrollRect.viewport != null ? scrollRect.viewport : (RectTransform)contentRoot.parent;
            var viewportHeight = viewport.rect.height > 1f ? viewport.rect.height : 1500f;
            var contentHeight = contentRoot.rect.height > 1f ? contentRoot.rect.height : contentRoot.sizeDelta.y;
            var levelPosition = GetLevelPosition(levelNumber);
            var target = -levelPosition.y - viewportHeight * 0.52f;
            return Mathf.Clamp(target, 0f, Mathf.Max(0f, contentHeight - viewportHeight));
        }

        private int GetInitialFocusLevel()
        {
            return Mathf.Clamp(GetSelectedOrUnlockedLevel(), CharacterMatch3Constants.FirstLevel, maxLevelNumber);
        }

        private int GetSelectedOrUnlockedLevel()
        {
            var selected = Mathf.Clamp(GameState.SelectedLevelNumber, CharacterMatch3Constants.FirstLevel, maxLevelNumber);
            if (CanPlayLevel(selected) && SaveManager.IsLevelUnlocked(selected))
            {
                return selected;
            }

            var highest = Mathf.Clamp(SaveManager.Data.highestUnlockedLevel, CharacterMatch3Constants.FirstLevel, maxLevelNumber);
            for (var levelNumber = highest; levelNumber >= CharacterMatch3Constants.FirstLevel; levelNumber--)
            {
                if (CanPlayLevel(levelNumber) && SaveManager.IsLevelUnlocked(levelNumber))
                {
                    return levelNumber;
                }
            }

            return CharacterMatch3Constants.FirstLevel;
        }

        private bool CanPlayLevel(int levelNumber)
        {
            return levelLibrary == null || levelLibrary.GetLevel(levelNumber) != null;
        }

        private Vector2 GetLevelPosition(int levelNumber)
        {
            var clamped = Mathf.Clamp(levelNumber, CharacterMatch3Constants.FirstLevel, maxLevelNumber);
            var zeroBased = clamped - CharacterMatch3Constants.FirstLevel;
            var sectionFromBottom = zeroBased / LevelsPerMapSegment;
            var localIndex = zeroBased % LevelsPerMapSegment;
            var localPosition = GetLevelPositionFactor(sectionFromBottom, localIndex);
            var x = GetMapViewportWidth() * localPosition.x;
            var y = GetSegmentTopY(sectionFromBottom) - GetSegmentHeight(sectionFromBottom) * localPosition.y;
            return new Vector2(x, y);
        }

        private Vector2 GetLevelPositionFactor(int segmentFromBottom, int localIndex)
        {
            var factors = GetLevelPositionFactors(segmentFromBottom);
            if (factors == null || factors.Length == 0)
            {
                return new Vector2(0.5f, 0.5f);
            }

            return factors[Mathf.Clamp(localIndex, 0, factors.Length - 1)];
        }

        private Vector2[] GetLevelPositionFactors(int segmentFromBottom)
        {
            return GetSegmentThemeIndex(segmentFromBottom) switch
            {
                0 => forestLevelPositionFactors,
                1 => beachLevelPositionFactors,
                _ => desertLevelPositionFactors
            };
        }

        private Vector2 GetMarkerPosition(int levelNumber)
        {
            if (usesSceneEditableMap)
            {
                return GetSceneEditableLevelLocalPosition(levelNumber) + SceneMarkerOffset;
            }

            return GetLevelPosition(levelNumber) + new Vector2(0f, 82f);
        }

        private Vector2 GetSceneEditableLevelLocalPosition(int levelNumber)
        {
            if (!nodeViews.TryGetValue(levelNumber, out var view) || view.Rect == null || contentRoot == null)
            {
                return Vector2.zero;
            }

            var worldCenter = view.Rect.TransformPoint(view.Rect.rect.center);
            var localCenter = contentRoot.InverseTransformPoint(worldCenter);
            return new Vector2(localCenter.x, localCenter.y);
        }

        private void AnimateCurrentLevelGlow()
        {
            if (highlightedLevelNumber <= 0 || !nodeViews.TryGetValue(highlightedLevelNumber, out var view) || view.Glow == null || !view.Glow.enabled)
            {
                return;
            }

            var pulse = 0.5f + 0.5f * Mathf.Sin(Time.unscaledTime * 3.2f);
            view.Glow.rectTransform.localScale = Vector3.one * Mathf.Lerp(0.98f, 1.1f, pulse);
            view.Glow.rectTransform.localRotation = Quaternion.Euler(0f, 0f, Mathf.Sin(Time.unscaledTime * 1.6f) * 4f);
            var color = view.Glow.color;
            color.a = Mathf.Lerp(0.12f, 0.28f, pulse);
            view.Glow.color = color;
        }

        private void AnimateIdleMarker()
