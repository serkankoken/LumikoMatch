        private IEnumerator AnimateAmbientSparkles()
        {
            const int count = 18;
            var rects = new RectTransform[count];
            var images = new Image[count];
            var centers = new Vector2[count];
            var phases = new float[count];
            var random = new System.Random(2407);

            for (var i = 0; i < count; i++)
            {
                var sparkle = UIFactory.CreateImage($"Sparkle_{i:00}", ambientSparkleRoot, GetSparkleColor(i));
                sparkle.raycastTarget = false;
                var rect = sparkle.rectTransform;
                rect.anchorMin = new Vector2(0.5f, 0.5f);
                rect.anchorMax = new Vector2(0.5f, 0.5f);
                rect.pivot = new Vector2(0.5f, 0.5f);
                rect.sizeDelta = Vector2.one * (9f + i % 4 * 3f);
                rects[i] = rect;
                images[i] = sparkle;
                centers[i] = new Vector2((float)(random.NextDouble() * 880f - 440f), (float)(random.NextDouble() * 1380f - 690f));
                phases[i] = (float)random.NextDouble() * Mathf.PI * 2f;
            }

            while (ambientSparkleRoot != null)
            {
                var time = Time.unscaledTime;
                for (var i = 0; i < count; i++)
                {
                    if (rects[i] == null || images[i] == null)
                    {
                        continue;
                    }

                    var bob = Mathf.Sin(time * (0.65f + i * 0.03f) + phases[i]);
                    rects[i].anchoredPosition = centers[i] + new Vector2(Mathf.Sin(time * 0.28f + i) * 22f, bob * 18f);
                    rects[i].localRotation = Quaternion.Euler(0f, 0f, time * (28f + i % 5 * 6f));
                    rects[i].localScale = Vector3.one * (0.76f + Mathf.Abs(bob) * 0.42f);
                    var color = images[i].color;
                    color.a = 0.18f + Mathf.Abs(bob) * 0.28f;
                    images[i].color = color;
                }

                yield return null;
            }
        }

        private void TryStartLevel(int levelNumber)
        {
            if (progressionPlaying || !CanPlayLevel(levelNumber) || !SaveManager.IsLevelUnlocked(levelNumber))
            {
                return;
            }

            AudioManager.Instance?.PlayButton();
            GameState.ClearMapProgression();
            GameState.ClearMapStarReveal();
            GameState.SelectedLevelNumber = levelNumber;
            SceneManager.LoadScene("Gameplay");
        }

        private IEnumerator PlayPendingProgressionIfNeeded()
        {
            yield return null;
            Canvas.ForceUpdateCanvases();

            var hasPendingStarReveal = GameState.HasPendingMapStarReveal;
            var pendingStarRevealLevel = Mathf.Clamp(GameState.PendingMapStarRevealLevel, CharacterMatch3Constants.FirstLevel, maxLevelNumber);
            var pendingStarRevealCount = Mathf.Clamp(GameState.PendingMapStarRevealCount, 1, 3);

            if (!GameState.HasPendingMapProgression)
            {
                progressionPlaying = hasPendingStarReveal;
                RefreshNodeStates(hasPendingStarReveal ? pendingStarRevealLevel : 0);
                SnapScrollToLevel(hasPendingStarReveal ? pendingStarRevealLevel : GetInitialFocusLevel());
                if (hasPendingStarReveal)
                {
                    yield return new WaitForSeconds(0.18f);
                    yield return AnimateStarsForLevel(pendingStarRevealLevel, pendingStarRevealCount);
                    GameState.ClearMapStarReveal();
                    progressionPlaying = false;
                    RefreshNodeStates();
                }

                yield break;
            }

            var fromLevel = Mathf.Clamp(GameState.PendingMapProgressFrom, CharacterMatch3Constants.FirstLevel, maxLevelNumber);
            var toLevel = Mathf.Clamp(GameState.PendingMapProgressTo, CharacterMatch3Constants.FirstLevel, maxLevelNumber);
            if (toLevel <= fromLevel)
            {
                GameState.ClearMapProgression();
                yield break;
            }

            progressionPlaying = true;
            var revealCompletedLevelStars = hasPendingStarReveal && pendingStarRevealLevel == fromLevel;
            RefreshNodeStates(revealCompletedLevelStars ? fromLevel : 0);
            SnapScrollToLevel(fromLevel);
            PlaceMarkerAtLevel(fromLevel);
            if (progressLabel != null)
            {
                progressLabel.text = $"Level {fromLevel} Complete!";
            }

            yield return new WaitForSeconds(0.18f);
            if (revealCompletedLevelStars)
            {
                yield return AnimateStarsForLevel(fromLevel, pendingStarRevealCount);
                GameState.ClearMapStarReveal();
                yield return new WaitForSeconds(0.1f);
            }

            for (var level = fromLevel; level < toLevel; level++)
            {
                yield return AnimateScrollToLevel(level + 1, 0.22f);
                yield return AnimateMarker(GetMarkerPosition(level), GetMarkerPosition(level + 1));
                GameState.SelectedLevelNumber = level + 1;
                RefreshNodeStates();
                yield return PulseNode(level + 1);
                yield return new WaitForSeconds(stepPauseDuration);
            }

            if (progressLabel != null)
            {
                progressLabel.text = $"Level {toLevel} Unlocked";
            }

            GameState.ClearMapProgression();
            GameState.ClearMapStarReveal();
            progressionPlaying = false;
            RefreshNodeStates();
            yield return new WaitForSeconds(0.45f);
            if (progressLabel != null)
            {
                progressLabel.text = string.Empty;
            }
        }

        private IEnumerator AnimateMarker(Vector2 from, Vector2 to)
        {
            if (playerMarkerRect == null)
            {
                yield break;
            }

            var markerRect = playerMarkerRect;
            for (var elapsed = 0f; elapsed < stepAnimationDuration; elapsed += Time.unscaledDeltaTime)
            {
                var t = Mathf.Clamp01(elapsed / stepAnimationDuration);
                var eased = EaseInOut(t);
                var arc = Mathf.Sin(t * Mathf.PI) * 56f;
                var position = Vector2.Lerp(from, to, eased) + new Vector2(0f, arc);
                if (usesSceneEditableMap)
                {
                    markerRect.localPosition = new Vector3(position.x, position.y, markerRect.localPosition.z);
                }
                else
                {
                    markerRect.anchoredPosition = position;
                }

                markerRect.localScale = playerMarkerBaseScale * (1f + Mathf.Sin(t * Mathf.PI) * 0.12f);
                yield return null;
            }

            if (usesSceneEditableMap)
            {
                markerRect.localPosition = new Vector3(to.x, to.y, markerRect.localPosition.z);
            }
            else
            {
                markerRect.anchoredPosition = to;
            }

            markerRect.localScale = playerMarkerBaseScale;
            SpawnMapBurst(to, new Color(1f, 0.88f, 0.28f, 1f), 16, 82f);
            HapticsManager.Medium();
        }

        private IEnumerator PulseNode(int levelNumber)
        {
            if (!nodeViews.TryGetValue(levelNumber, out var view))
            {
                yield break;
            }

            var rect = view.Button.GetComponent<RectTransform>();
            var baseScale = view.BaseScale == Vector3.zero ? Vector3.one : view.BaseScale;
            for (var elapsed = 0f; elapsed < 0.32f; elapsed += Time.unscaledDeltaTime)
            {
                var t = Mathf.Clamp01(elapsed / 0.32f);
                rect.localScale = baseScale * (1f + Mathf.Sin(t * Mathf.PI) * 0.18f);
                yield return null;
            }

            rect.localScale = baseScale;
            var burstPosition = usesSceneEditableMap ? GetSceneEditableLevelLocalPosition(levelNumber) : rect.anchoredPosition;
            SpawnMapBurst(burstPosition, new Color(0.35f, 0.78f, 1f, 1f), 14, 76f);
        }

        private IEnumerator AnimateStarsForLevel(int levelNumber, int stars)
        {
            if (!nodeViews.TryGetValue(levelNumber, out var view) || view.StarsLabel == null)
            {
                yield break;
            }

            stars = Mathf.Clamp(stars, 1, 3);
            var rect = view.StarsLabel.rectTransform;
            var baseScale = rect.localScale == Vector3.zero ? Vector3.one : rect.localScale;
            view.StarsLabel.text = string.Empty;
            view.StarsLabel.color = new Color(1f, 0.86f, 0.18f, 1f);
            view.StarsLabel.transform.SetAsLastSibling();

            for (var visibleStars = 1; visibleStars <= stars; visibleStars++)
            {
                view.StarsLabel.text = BuildStarText(visibleStars);
                var burstPosition = usesSceneEditableMap
                    ? GetSceneEditableLevelLocalPosition(levelNumber) + new Vector2(0f, -66f)
                    : GetLevelPosition(levelNumber) + new Vector2(0f, -66f);
                SpawnMapBurst(burstPosition, new Color(1f, 0.86f, 0.18f, 1f), 8 + visibleStars * 3, 62f);
                HapticsManager.Light();
                for (var elapsed = 0f; elapsed < 0.22f; elapsed += Time.unscaledDeltaTime)
                {
                    var t = Mathf.Clamp01(elapsed / 0.22f);
                    var bounce = 1f + Mathf.Sin(t * Mathf.PI) * 0.45f;
                    rect.localScale = baseScale * bounce;
                    yield return null;
                }

                rect.localScale = baseScale;
                yield return new WaitForSeconds(0.08f);
            }

            yield return PulseNode(levelNumber);
        }

        private IEnumerator AnimateScrollToLevel(int levelNumber, float duration)
        {
            if (usesSceneEditableMap || scrollRect == null || contentRoot == null)
            {
                yield break;
            }

            Canvas.ForceUpdateCanvases();
            var start = contentRoot.anchoredPosition;
            var target = new Vector2(start.x, GetScrollYForLevel(levelNumber));
            for (var elapsed = 0f; elapsed < duration; elapsed += Time.unscaledDeltaTime)
            {
                var t = Mathf.Clamp01(elapsed / duration);
                contentRoot.anchoredPosition = Vector2.Lerp(start, target, EaseInOut(t));
                yield return null;
            }

            contentRoot.anchoredPosition = target;
        }

        private void RefreshNodeStates(int suppressStarsForLevel = 0)
        {
            if (usesSceneEditableMap)
            {
                RefreshSceneEditableMapButtons();
                return;
            }

            highlightedLevelNumber = 0;
            foreach (var pair in nodeViews)
            {
                var levelNumber = pair.Key;
                var view = pair.Value;
                var unlocked = CanPlayLevel(levelNumber) && SaveManager.IsLevelUnlocked(levelNumber);
                var current = unlocked && levelNumber == GetSelectedOrUnlockedLevel();
                var stars = SaveManager.GetStars(levelNumber);
                var completed = unlocked && stars > 0;
                if (current)
                {
                    highlightedLevelNumber = levelNumber;
                }

                view.Button.interactable = unlocked && !progressionPlaying;
                view.Image.sprite = current && currentNodeSprite != null
                    ? currentNodeSprite
                    : completed && completedNodeSprite != null
                        ? completedNodeSprite
                        : unlocked && unlockedNodeSprite != null
                            ? unlockedNodeSprite
                            : !unlocked && lockedNodeSprite != null
                                ? lockedNodeSprite
                                : null;
                view.Image.color = view.Image.sprite != null
                    ? Color.white
                    : current
                        ? new Color(0.2f, 0.68f, 1f, 0.98f)
                        : completed
                            ? new Color(0.46f, 0.82f, 0.16f, 0.98f)
                            : unlocked
                                ? new Color(0.23f, 0.66f, 0.94f, 0.98f)
                                : new Color(0.45f, 0.48f, 0.57f, 0.86f);

                view.NumberLabel.text = unlocked ? levelNumber.ToString() : "?";
                view.NumberLabel.fontSize = unlocked ? 48 : 40;
                view.NumberLabel.resizeTextMaxSize = unlocked ? 48 : 40;
                view.NumberLabel.resizeTextMinSize = unlocked ? 25 : 14;
                view.NumberLabel.color = unlocked ? Color.white : new Color(0.9f, 0.86f, 0.78f, 0.82f);
                var displayStars = levelNumber == suppressStarsForLevel ? 0 : stars;
                view.StarsLabel.text = unlocked && displayStars > 0 ? BuildStarText(displayStars) : string.Empty;
                view.StarsLabel.rectTransform.localScale = Vector3.one;
                view.BadgeLabel.text = string.Empty;
                view.BadgeLabel.color = Color.white;
                if (view.BadgePlate != null)
                {
                    view.BadgePlate.enabled = false;
                }

                if (view.Glow != null)
                {
                    view.Glow.enabled = current;
                    view.Glow.color = current ? new Color(1f, 0.88f, 0.28f, 0.2f) : Color.clear;
                    view.Glow.rectTransform.localScale = Vector3.one;
                }
            }

            PlaceMarkerAtLevel(GetSelectedOrUnlockedLevel());
            playerMarker?.transform.SetAsLastSibling();
            progressLabel?.transform.SetAsLastSibling();
        }

